enum class Modifier(val bit: Int) {
    SHIFT(0x1),
    CONTROL(0x2),
    ALT(0x4),
    SUPER(0x8);

    fun isSet(modifiers: Int): Boolean {
        return modifiers.and(bit) != 0
    }
}
